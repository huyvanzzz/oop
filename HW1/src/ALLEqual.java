public class ALLEqual {
    public static void main(String[] args) {
        int a = Integer.parseInt("2");
        int b = Integer.parseInt("1");
        int c = Integer.parseInt("0");

        if (a == b && a == c) {
            System.out.println("all equal");
        }
        else {
            System.out.println("not all equal");
        }

    }
}
